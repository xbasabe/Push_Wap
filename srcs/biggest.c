#include"Push_swap.h"

int	biggest(int *stack_a)
{
	int	i;
	int	c;

	c = 1;
	i = c;
	while (c <= stack_a[0])
	{
		if (stack_a[c] > stack_a[i])
			i = c;
		c ++;
	}
	return (i);
}
